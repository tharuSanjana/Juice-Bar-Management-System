    public void loadAllItemIngredient() {
        var model = new ItemDetailsModel();

        try {
            List<ItemDetailsDto> dtoList = model.getAll();
            DefaultTableModel tableModel = (DefaultTableModel) tblItemDetails.getModel();
            tableModel.setRowCount(0);

            // Add rows to the table
            for (ItemDetailsDto dto : dtoList) {
                Object[] rowData = {
                    dto.getItemId(),
                    dto.getItemName(),
                    dto.getIngId(),
                    dto.getIngName()

                };
                tableModel.addRow(rowData);  // <-- Correct way
            }

            // Optionally select the first row
            if (tableModel.getRowCount() > 0) {
                tblItemDetails.setRowSelectionInterval(0, 0);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error loading itemDetails data:\n" + e.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE
            );
            e.printStackTrace();
        }
    }

