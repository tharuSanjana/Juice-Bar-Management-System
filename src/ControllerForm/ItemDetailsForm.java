    private void SaveBtnOnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnOnActionActionPerformed
        // TODO add your handling code here:
        ItemDetailsModel model = new ItemDetailsModel();
        String itemId = cmbItemId.getSelectedItem().toString();
        String itemName = txtItemName.getText();
        String ingId = cmbIngredientId.getSelectedItem().toString();
        String ingName = txtIngName.getText();

        var dto = new ItemDetailsDto(itemId, ingId);

        boolean flag = false;
        try {
            flag = model.save(dto);
        } catch (SQLException ex) {
            System.getLogger(CustomerForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        if (flag) {
            JOptionPane.showMessageDialog(this,
                    "Item saved successfully! ",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Failed to save Item.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_SaveBtnOnActionActionPerformed

